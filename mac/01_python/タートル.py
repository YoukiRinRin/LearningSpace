import turtle
my_turtle = turtle.Turtle()
screen = turtle.Screen()
screen.setup(800,800)
screen.title("タートル")
my_turtle.shape("turtle")
my_turtle.pensize(4)


my_turtle.forward(150)
my_turtle.left(90)
my_turtle.forword(150)
my_turtle.right(90)
my_turtle.forward(150)
my_turtle.left(90)
my_turtle.forward(150)

screen.mainloop()